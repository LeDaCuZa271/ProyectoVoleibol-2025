
public class Persona {
	private int cod;
	private String nombre, ci, usuario,password;
	
	public Persona(int cod, String nombre, String ci, String usuario, String password) {
		this.cod = cod;
		this.nombre = nombre;
		this.ci = ci;
		this.usuario = usuario;
		this.password = password;
	}

	public int getCod() {
		return cod;
	}

	public void setCod(int cod) {
		this.cod = cod;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getCi() {
		return ci;
	}

	public void setCi(String ci) {
		this.ci = ci;
	}

	public String getUsuario() {
		return usuario;
	}

	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}
	
	
	
	
}
